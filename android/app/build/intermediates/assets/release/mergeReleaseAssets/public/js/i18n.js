function t(key) {
    var lang = (typeof state !== 'undefined' && state.lang) || 'es';
    var dict = LANG[lang] || LANG.es;
    return dict[key] !== undefined ? dict[key] : (LANG.es[key] || key);
}

function applyLanguage() {
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
        var key = el.getAttribute('data-i18n');
        if (!key) return;
        el.textContent = t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
        var key = el.getAttribute('data-i18n-placeholder');
        if (!key) return;
        el.placeholder = t(key);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
        var key = el.getAttribute('data-i18n-title');
        if (!key) return;
        el.title = t(key);
    });
    if (typeof updateTocallFields === 'function') updateTocallFields();
}

function setLanguage(lang) {
    if (!LANG[lang]) return;
    state.lang = lang;
    persistSettings();
    applyLanguage();
    if (document.getElementById('setLang')) {
        document.getElementById('setLang').value = lang;
    }
}
